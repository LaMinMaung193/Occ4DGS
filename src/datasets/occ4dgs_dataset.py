"""
Occ4DGS dataset: builds the same per-sample results dict shape as GaussianFormer3D's
NuScenesDataset (dataset/dataset.py get_data_info), but sources frames from
src/datasets/nuscenes_mini.py + src/datasets/occ3d_gt.py instead of their external pkl.

Confirmed against GaussianFormer3D source (Phase 2 investigation, EXPERIMENT_LOG.md):
  - get_img2global / get_lidar2global (dataset/utils.py) take calib_dict/pose_dict with
    keys {rotation, translation, camera_intrinsic} -- identical to nuscenes-devkit's
    calibrated_sensor / ego_pose records, no adapter needed.
  - LoadOccupancyOcc3d (dataset/transform_3d.py) already handles Occ3D-format GT natively;
    it reads os.path.join(self.occ_path, results['occ_path']) -- we fully control the
    'occ_path' string per-sample, so we resolve it here using the SAME dual-layout check
    (subfolder/labels.npz vs flat <token>.npz) already proven in occ3d_gt.py Phase 1.
  - LoadMultiViewDepthFromFiles expects <root>/depth_gt/<img_basename>.bin -- generated by
    scripts/generate_depth_gt.py (LiDAR-to-camera projection), not downloaded.
"""
import os
import numpy as np
from pyquaternion import Quaternion
from torch.utils.data import Dataset

import sys
sys.path.insert(0, os.path.join(os.path.dirname(__file__), "..", "..", "..", "GaussianFormer3D"))
from dataset.utils import get_img2global, get_lidar2global  # reused unmodified, see docstring

from .nuscenes_mini import CAM_NAMES, LIDAR_NAME, get_sample_sensor_paths


def _resolve_occ_path(occ3d_gts_root, scene_name, sample_token):
    """Same dual-layout check as occ3d_gt.load_occ3d_labels, but returns the path
    RELATIVE to occ3d_gts_root (what LoadOccupancyOcc3d expects in results['occ_path']),
    not the loaded array. Returns None if neither layout has this sample's GT."""
    candidates = [
        os.path.join(scene_name, sample_token, "labels.npz"),
        os.path.join(scene_name, sample_token + ".npz"),
    ]
    for rel in candidates:
        if os.path.exists(os.path.join(occ3d_gts_root, rel)):
            return rel
    return None


class Occ4DGSDataset(Dataset):
    """
    frame_index: the dict loaded from experiments/phase1_frame_index.json
                 ({scene_name: [{"sample_token":..., "has_gt": bool}, ...]})
    Only entries with has_gt=True are included -- Phase 1 confirmed 100% coverage across
    all 10 scenes, but this filter is kept so the dataset stays correct if that ever changes.
    """

    def __init__(self, nusc, frame_index, nuscenes_root, occ3d_gts_root, pipeline):
        self.nusc = nusc
        self.nuscenes_root = nuscenes_root
        self.occ3d_gts_root = occ3d_gts_root
        self.pipeline = pipeline

        self.samples = []  # list of (scene_name, sample_token, occ_path_rel)
        for scene_name, entries in frame_index.items():
            for e in entries:
                if not e["has_gt"]:
                    continue
                occ_rel = _resolve_occ_path(occ3d_gts_root, scene_name, e["sample_token"])
                assert occ_rel is not None, (
                    f"has_gt=True but no GT file found for {scene_name}/{e['sample_token']} "
                    f"-- frame index is stale, rerun scripts/build_frame_index.py"
                )
                self.samples.append((scene_name, e["sample_token"], occ_rel))

    def __len__(self):
        return len(self.samples)

    def get_data_info(self, scene_name, sample_token, occ_path_rel):
        sample = self.nusc.get("sample", sample_token)
        lidar_sd = self.nusc.get("sample_data", sample["data"][LIDAR_NAME])
        lidar_calib = self.nusc.get("calibrated_sensor", lidar_sd["calibrated_sensor_token"])
        lidar_pose = self.nusc.get("ego_pose", lidar_sd["ego_pose_token"])

        lidar2ego = np.eye(4)
        lidar2ego[:3, :3] = Quaternion(lidar_calib["rotation"]).rotation_matrix
        lidar2ego[:3, 3] = np.array(lidar_calib["translation"]).T
        ego2lidar = np.linalg.inv(lidar2ego)

        lidar2global = get_lidar2global(lidar_calib, lidar_pose)
        ego2global = np.eye(4)
        ego2global[:3, :3] = Quaternion(lidar_pose["rotation"]).rotation_matrix
        ego2global[:3, 3] = np.asarray(lidar_pose["translation"]).T

        image_paths, lidar2img_rts, img2lidar_rts = [], [], []
        cam_intrinsics, cam2ego_rts = [], []

        sensor_paths = get_sample_sensor_paths(self.nusc, sample_token)
        for cam in CAM_NAMES:
            image_paths.append(sensor_paths[cam])
            cam_sd = self.nusc.get("sample_data", sample["data"][cam])
            cam_calib = self.nusc.get("calibrated_sensor", cam_sd["calibrated_sensor_token"])
            cam_pose = self.nusc.get("ego_pose", cam_sd["ego_pose_token"])

            img2global = get_img2global(cam_calib, cam_pose)
            lidar2img = np.linalg.inv(img2global) @ lidar2global
            img2lidar = np.linalg.inv(lidar2global) @ img2global

            cam2ego = np.eye(4)
            cam2ego[:3, :3] = Quaternion(cam_calib["rotation"]).rotation_matrix
            cam2ego[:3, 3] = np.array(cam_calib["translation"]).T

            intrinsic = cam_calib["camera_intrinsic"]
            viewpad = np.eye(4)
            viewpad[:3, :3] = np.asarray(intrinsic)

            lidar2img_rts.append(lidar2img)
            img2lidar_rts.append(img2lidar)
            cam_intrinsics.append(viewpad)
            cam2ego_rts.append(cam2ego)

        return dict(
            sample_idx=sample_token,
            pts_filename=sensor_paths[LIDAR_NAME],
            lidar_path=sensor_paths[LIDAR_NAME],    # <-- ADD THIS: same file, separate key some transforms 
            sweeps=self._get_sweeps(sample_token),   # <-- ADD THIS
            occ_path=occ_path_rel,
            timestamp=sample["timestamp"] / 1e6,
            ego2global=ego2global,
            lidar2global=lidar2global,
            img_filename=image_paths,
            lidar2img=np.asarray(lidar2img_rts),
            img2lidar=np.asarray(img2lidar_rts),
            cam_intrinsic=np.asarray(cam_intrinsics),
            ori_intrinsic=np.array(cam_intrinsics).copy(),
            ego2lidar=ego2lidar,
            scene_token=scene_name,
        )


    def _get_sweeps(self, sample_token, max_sweeps=10):
        """Build the sweeps list LoadPointsFromMultiSweepsLiDAR expects: each entry needs
        data_path, timestamp (RAW microseconds -- the transform itself divides by 1e6,
        matching their own get_data_info convention, do not pre-convert here), and the
        sensor2lidar rotation/translation chaining the sweep's LiDAR frame into the
        CURRENT frame's LiDAR frame. Logic ported from GaussianFormer3D's
        tools/make_gf3d_infos.py obtain_sensor2top(), adapted to call nuscenes-devkit
        directly instead of reading from their pkl."""
        sample = self.nusc.get("sample", sample_token)
        cur_lidar_sd = self.nusc.get("sample_data", sample["data"][LIDAR_NAME])
        cur_lidar_calib = self.nusc.get("calibrated_sensor", cur_lidar_sd["calibrated_sensor_token"])
        cur_lidar_pose = self.nusc.get("ego_pose", cur_lidar_sd["ego_pose_token"])

        l2e_r_mat = Quaternion(cur_lidar_calib["rotation"]).rotation_matrix
        l2e_t = np.array(cur_lidar_calib["translation"])
        e2g_r_mat = Quaternion(cur_lidar_pose["rotation"]).rotation_matrix
        e2g_t = np.array(cur_lidar_pose["translation"])

        sweeps = []
        sd_rec = cur_lidar_sd
        while len(sweeps) < max_sweeps and sd_rec["prev"] != "":
            sd_rec = self.nusc.get("sample_data", sd_rec["prev"])
            cs_record = self.nusc.get("calibrated_sensor", sd_rec["calibrated_sensor_token"])
            pose_record = self.nusc.get("ego_pose", sd_rec["ego_pose_token"])

            l2e_r_s_mat = Quaternion(cs_record["rotation"]).rotation_matrix
            l2e_t_s = np.array(cs_record["translation"])
            e2g_r_s_mat = Quaternion(pose_record["rotation"]).rotation_matrix
            e2g_t_s = np.array(pose_record["translation"])

            R = (l2e_r_s_mat.T @ e2g_r_s_mat.T) @ (np.linalg.inv(e2g_r_mat).T @ np.linalg.inv(l2e_r_mat).T)
            T = (l2e_t_s @ e2g_r_s_mat.T + e2g_t_s) @ (np.linalg.inv(e2g_r_mat).T @ np.linalg.inv(l2e_r_mat).T)
            T -= e2g_t @ (np.linalg.inv(e2g_r_mat).T @ np.linalg.inv(l2e_r_mat).T) + l2e_t @ np.linalg.inv(l2e_r_mat).T

            sweeps.append({
                "data_path": os.path.join(self.nusc.dataroot, sd_rec["filename"]),
                "timestamp": sd_rec["timestamp"],          # RAW microseconds, matches their convention
                "sensor2lidar_rotation": R.T,
                "sensor2lidar_translation": T,
            })
        return sweeps

    def __getitem__(self, idx):
        scene_name, sample_token, occ_path_rel = self.samples[idx]
        input_dict = self.get_data_info(scene_name, sample_token, occ_path_rel)
        for t in self.pipeline:
            input_dict = t(input_dict)

        return_dict = {
            "img": input_dict["img"],
            "projection_mat": input_dict["projection_mat"],
            "image_wh": input_dict["image_wh"],
            "occ_label": input_dict["occ_label"],
            "occ_xyz": input_dict["occ_xyz"],
            "occ_cam_mask": input_dict["occ_cam_mask"],
        }
        for k in ("points", "img_filename", "dpt", "occ3d_mask_camera", "pts_filename"):
            if k in input_dict:
                return_dict[k] = input_dict[k]
        return return_dict